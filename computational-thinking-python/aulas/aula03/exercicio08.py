##8. Calcular e exibir o perímetro de um círculo, dado o valor do raio.
## Formula: perímetro = 2 * π * raio

raio = float(input("Digite o valor do raio: "))
print(f"Você digitou: {raio} de raio")
pi = 3.1416

perimetro = 2 * pi * raio
print(f"Dado o raio de {raio}, o perímetro é: {perimetro:.2f}")

